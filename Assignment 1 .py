# Task 1

# Basic Arithmetic operations

a1 = int(input("Enter first number: "))
a2 = int(input("Enter second number: "))

# Mathematical Operations
addition = a1 + a2
subtraction = a1 - a2
multiplication = a1 * a2
division = a1 / a2

# Displaying the results
print(f"Addition: {a1} + {a2} = {addition}")
print(f"Subtraction: {a1} - {a2} = {subtraction}")
print(f"Multiplication: {a1} * {a2} = {multiplication}")
print(f"Division: {a1} / {a2} = {division}")


